package com.pradeep;

public class Primitives {
    public static void main(String[] args) {
        //Primitive data types are the ones which cannot be broken into further parts
        int rollno=64;
        char letter='r';//char starts with sinle quote while string with double
        float r=0.98f;//f is added at end because all decimal are default of double datatype
        double largedecimal=3543643.3464564;
        long largeinteger=6364575674477L;//default datatype for number without decimals are of int
        boolean check=true;



        //String is not primitive datatype since it can be broken to characters
        String name="Pradeep";
    }
}
